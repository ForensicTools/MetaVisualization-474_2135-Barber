USE MetaViz;
DROP TABLE IF EXISTS images;
CREATE TABLE images (
	hash varchar(68) NOT NULL PRIMARY KEY,
	name varchar(150),
	FileSize varchar(10),
	FilePermissions char(9),
	FileType varchar(10),
	MIMEType varchar(20),
	ExifByteOrder varchar(50),
	Make varchar(20),
	CameraModelName varchar(50),
	Orientation varchar(30),
	XResolution int,
	YResolution int,
	ResolutionUnit varchar(20),
	ModifyDate datetime,
	YCbCrPositioning varchar(20),
	ExposureTime varchar(15),
	FNumber decimal(5,1),
	ExifVersion int(4),
	DateTimeOriginal datetime,
	CreateDate datetime,
	ComponentsConfiguration varchar(20),
	ShutterSpeedValue varchar(20),
	ApertureValue varchar(50),
	BrightnessValue float,
	MaxApertureValue varchar(50),
	LightSource varchar(20),
	Flash varchar(50),
	FocalLength varchar(10),
	Warning varchar(50),
	FlashpixVersion int(4),
	ColorSpace varchar(10),
	ExifImageWidth int,
	ExifImageHeight int,
	InteroperabilityIndex varchar(50),
	InteroperabilityVersion int(4),
	SceneType varchar(50),
	CustomRendered varchar(20),
	ExposureMode varchar(50),
	WhiteBalance varchar(50),
	DigitalZoomRatio int,
	SceneCaptureType varchar(50),
	Contrast varchar(50),
	Saturation varchar(50),
	Sharpness varchar(50),
	GPSVersionID varchar(10),
	GPSLatitudeRef varchar(5),
	GPSLongitudeRef varchar(4),
	GPSAltitudeRef varchar(50),
	GPSTimeStamp time,
	GPSMapDatum varchar(50),
	GPSProcessingMethod varchar(50),
	GPSDateStamp date,
	Compression varchar(50),
	ThumbnailOffset int,
	ThumbnailLength int,
	ImageWidth int,
	ImageHeight int,
	EncodingProcess varchar(50),
	BitsPerSample int,
	ColorComponents int,
	YCbCrSubSampling varchar(50),
	Aperture decimal(3,1),
	GPSAltitude varchar(50),
	GPSDateTime datetime,
	GPSLatitude varchar(50),
	GPSLongitude varchar(50),
	GPSPosition varchar(50),
	ImageSize varchar(20),
	ShutterSpeed varchar(10),
	ThumbnailImage varchar(200)
	);
